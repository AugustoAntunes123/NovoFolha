    private void lbGerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lbGerarActionPerformed
        String nome;
        double quantHoras, valorHora, quantFilhos, quantHorasExtras, salBruto, 
                salHorasExtras, salFamilia, descontoInss = 0, descontoIr, 
                salarioLiquido;
        DecimalFormat f = new DecimalFormat("0.0");
        
        quantHoras = Double.parseDouble(txtQH.getText());
        valorHora = Double.parseDouble(txtVH.getText());
        quantFilhos = Double.parseDouble(txtQF.getText());
        quantHorasExtras = Double.parseDouble(txtQHE.getText());
        nome = txtNome.getText();
        
        lbF.setText(nome);
        lbQH.setText(String.valueOf(f.format(quantHoras)));
        lbQHE.setText(String.valueOf(f.format(quantHorasExtras)));
        
        salBruto = quantHoras * valorHora;
        lbSB.setText(String.valueOf(f.format(salBruto)));
        salHorasExtras = quantHorasExtras * valorHora * 1.5;
        lbVHE.setText(String.valueOf(f.format(salHorasExtras)));
        salFamilia = quantFilhos * 15;
        lbVSF.setText(String.valueOf(f.format(salFamilia)));
        
        if (salBruto <=1320.00){
        descontoInss = 0.075 * salBruto;
        } else if (salBruto >1320.00 && salBruto <=2571.29){
            descontoInss = 0.09 * salBruto;
        } else if (salBruto >2571.29 && salBruto<=3856.94){
            descontoInss = 0.12 * salBruto;
        } else if(salBruto >3856.94){
            descontoInss = 0.14 * salBruto;
        }
        lbDIS.setText(String.valueOf(f.format(descontoInss)));
        
        if(salBruto <1903.09){
            descontoIr = 0;
        }else if(salBruto >=1903.09 && salBruto <=2826.65){
            descontoIr = salBruto * 0.075;
        }
        
    }//GEN-LAST:event_lbGerarActionPerformed

